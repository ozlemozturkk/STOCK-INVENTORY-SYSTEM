private void loadTable() {

    DefaultTableModel model = (DefaultTableModel) addproducttable.getModel();
    model.setRowCount(0);

    DBManagment db = new DBManagment();

    try (Connection con = DBManagment.connect()) {

        String sql = """
            SELECT 
                s.StoreName,
                p.ProductName,
                c.CategoryName,
                b.BrandName,
                sil.Quantity
            FROM StoreInventoryLevels sil
            JOIN Stores s ON sil.StoreID = s.StoreID
            JOIN Products p ON sil.ProductID = p.ProductID
            JOIN Brands b ON p.BrandID = b.BrandID
            JOIN Categories c ON p.CategoryID = c.CategoryID
            ORDER BY s.StoreName;
        """;

        PreparedStatement ps = con.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {

            Object[] row = new Object[5];

            row[0] = rs.getString("StoreName");
            row[1] = rs.getString("ProductName");
            row[2] = rs.getString("CategoryName");
            row[3] = rs.getString("BrandName");
            row[4] = rs.getInt("Quantity");

            model.addRow(row);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}
