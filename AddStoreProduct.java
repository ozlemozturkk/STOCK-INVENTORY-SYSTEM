    private void addstorebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addstorebuttonActionPerformed

        String storeName = storeCB.getSelectedItem().toString();
        String productName = productsCB.getSelectedItem().toString();
        int qty = Integer.parseInt(quantityTextField.getText());

        DBManagment db = new DBManagment();

        int storeID = db.getStoreIDByName(storeName);
        int productID = db.getProductIDByName(productName);

        boolean result = db.addProductToStore(storeID, productID, qty);

        if (result) {
            JOptionPane.showMessageDialog(this, "Product Added!");
            loadTable(); // tabloyu yenile
        } else {
            JOptionPane.showMessageDialog(this, "Hata oluştu!");
        }
    }//GEN-LAST:event_addstorebuttonActionPerformed

