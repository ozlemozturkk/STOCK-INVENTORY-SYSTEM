    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
   DBManagment db = new DBManagment();

String barcode = barcodeTextField.getText();

ComboItem selectedProduct = (ComboItem) productsCB.getSelectedItem();
ComboItem selectedCategory = (ComboItem) ComboItem.getSelectedItem();
ComboItem selectedBrand = (ComboItem) brandBox.getSelectedItem();

String productName = selectedProduct.getName();  // ✔ productID değil, name alıyoruz
int categoryID = selectedCategory.getId();
int brandID = selectedBrand.getId();

int newID = db.addProduct(productName, categoryID, brandID, barcode);

if (newID > 0) {
    db.createInitialStock(newID);
    JOptionPane.showMessageDialog(this, "Product saved!");
    loadProducts();
}

    
    
    }//GEN-LAST:event_saveButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
    int row = tblProducts.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a product!");
        return;
    }

    int id = Integer.parseInt(tblProducts.getValueAt(row, 0).toString());

    DBManagment db = new DBManagment();

    // ✔ product name ComboBox'tan alınmalı
    ComboItem selectedProduct = (ComboItem) productsCB.getSelectedItem();
    String productName = selectedProduct.getName();   // önemli

    String barcode = barcodeTextField.getText();

    ComboItem selectedCategory = (ComboItem) ComboItem.getSelectedItem();
    ComboItem selectedBrand = (ComboItem) brandBox.getSelectedItem();

    int categoryID = selectedCategory.getId();
    int brandID = selectedBrand.getId();

    // ✔ Artık DBManagment method imzasına uyuyor
    db.updateProduct(id, productName, categoryID, brandID, barcode);

    JOptionPane.showMessageDialog(this, "Product updated!");
    loadProducts();
    }//GEN-LAST:event_updateButtonActionPerformed


