private void selectComboItem(JComboBox<ComboItem> box, String name) {
    for (int i = 0; i < box.getItemCount(); i++) {
        ComboItem item = box.getItemAt(i);
        if (item.getName().equals(name)) {
            box.setSelectedIndex(i);
            return;
        }
    }
}

   
