  public void refreshProductList() {
    productsCB.removeAllItems();

    DBManagment db = new DBManagment();
    List<ComboItem> productList = db.getAllProductsAsCombo();

    for (ComboItem item : productList) {
        productsCB.addItem(item);
    }
}


    private void loadStores() {
    storeCB.removeAllItems();

    String sql = "SELECT StoreID, StoreName FROM Stores";

    try (Connection con = DBManagment.connect();
         PreparedStatement ps = con.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            storeCB.addItem(
                new ComboItem(rs.getString("StoreName"), rs.getInt("StoreID"))
            );
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}



         


