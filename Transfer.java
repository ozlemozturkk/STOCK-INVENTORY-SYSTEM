    private void depoTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_depoTableMouseClicked
 
        
       int row = depoTable.getSelectedRow();
    if (row == -1) return;

    // JTable'dan değerleri çekiyoruz
    String productName = depoTable.getValueAt(row, 0).toString();
    String categoryName = depoTable.getValueAt(row, 1).toString();
    String brandName = depoTable.getValueAt(row, 2).toString();
    String quantity = depoTable.getValueAt(row, 3).toString();

    // ✔ ComboBox'ta ilgili item’ı seçiyoruz
    selectComboBoxItem(productsCB, productName);
    selectComboBoxItem(categoryBox, categoryName);
    selectComboBoxItem(brandBox, brandName);

    // ✔ Quantity textfield kontrolü
    quantityTF.setText(quantity);
    }//GEN-LAST:event_depoTableMouseClicked

