    private void transferBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferBttnActionPerformed
 
  // 1) Ürün seçimi
ComboItem selectedProduct = (ComboItem) productsCB.getSelectedItem();
if (selectedProduct == null) {
    JOptionPane.showMessageDialog(this, "Lütfen ürün seçin!");
    return;
}
int productId = selectedProduct.getId();

// 2) Miktar kontrolü
String qtyText = quantityTF.getText().trim();
if (qtyText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Lütfen miktar girin!");
    return;
}

int quantity;
try {
    quantity = Integer.parseInt(qtyText);
} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, "Miktar sayı olmalıdır!");
    return;
}

if (quantity <= 0) {
    JOptionPane.showMessageDialog(this, "Miktar 0'dan büyük olmalıdır!");
    return;
}

// 3) Depo ID – Merkez Depo = 1
int warehouseId = 1;

// 4) Depodan ürün düşme (store eklemesi YOK!)
DBManagment db = new DBManagment();
boolean success = db.transferToStore(productId, warehouseId, quantity);

if (success) {
    JOptionPane.showMessageDialog(this, "✔ Depodan ürün başarıyla düşüldü!");
    loadWarehouseTable(); // varsa tablo yenile
} else {
    JOptionPane.showMessageDialog(this, "❌ Yetersiz stok veya işlem hatası!");
}

   


    }//GEN-LAST:event_transferBttnActionPerformed

