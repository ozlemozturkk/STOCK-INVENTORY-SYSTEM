    private void savebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebuttonActionPerformed
         String name = txtName.getText();
    String phone = txtPhone.getText();
    String email = txtEmail.getText();
    String address = txtAddress.getText();

    DBManagment db = new DBManagment();

    if (db.addSupplier(name, phone, email, address)) {
        JOptionPane.showMessageDialog(this, "Supplier added!");
        loadSupplierTable();
    } else {
        JOptionPane.showMessageDialog(this, "Error!");
    }
    }//GEN-LAST:event_savebuttonActionPerformed

    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        if (selectedSupplierID == -1) {
        JOptionPane.showMessageDialog(this, "Please select a supplier!");
        return;
    }

    String name = txtName.getText();
    String phone = txtPhone.getText();
    String email = txtEmail.getText();
    String address = txtAddress.getText();

    DBManagment db = new DBManagment();

    if (db.updateSupplier(selectedSupplierID, name, phone, email, address)) {
        JOptionPane.showMessageDialog(this, "Updated!");
        loadSupplierTable();
    } else {
        JOptionPane.showMessageDialog(this, "Error!");
    }
    }//GEN-LAST:event_updatebuttonActionPerformed

    private void supplierTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supplierTableMouseClicked
          int row = supplierTable.getSelectedRow();

    selectedSupplierID = Integer.parseInt(supplierTable.getValueAt(row, 0).toString());
    txtName.setText(supplierTable.getValueAt(row, 1).toString());
    txtPhone.setText(supplierTable.getValueAt(row, 2).toString());
    txtEmail.setText(supplierTable.getValueAt(row, 3).toString());
    txtAddress.setText(supplierTable.getValueAt(row, 4).toString());
    }//GEN-LAST:event_supplierTableMouseClicked

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
         if (selectedSupplierID == -1) {
        JOptionPane.showMessageDialog(this, "Select a row!");
        return;
    }

    DBManagment db = new DBManagment();

    if (db.deleteSupplier(selectedSupplierID)) {
        JOptionPane.showMessageDialog(this, "Deleted!");
        loadSupplierTable();
        selectedSupplierID = -1;
    } else {
        JOptionPane.showMessageDialog(this, "Error!");
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

