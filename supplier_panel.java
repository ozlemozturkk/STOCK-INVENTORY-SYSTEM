private void loadSupplierTable() {
    DBManagment db = new DBManagment();
    List<Object[]> suppliers = db.getSuppliers();

    DefaultTableModel model = (DefaultTableModel) supplierTable.getModel();
    model.setRowCount(0);

    for (Object[] row : suppliers) {
        model.addRow(row);
    }
}


